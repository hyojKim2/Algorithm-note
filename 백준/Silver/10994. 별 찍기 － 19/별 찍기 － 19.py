n=int(input())


def Star(k):
    if k==1:
        print("*"+" *"*(2*(n-1)))
    else:
        print("*" + " *"*(n-k) + "*"*(3+4*(k-2)) +"* "*(n-k) +"*")
        print("*" + " *"*(n-k) + " "*(3+4*(k-2)) +"* "*(n-k) +"*")
        Star(k-1)
        print("*" + " *"*(n-k) + " "*(3+4*(k-2)) +"* "*(n-k) +"*")
        print("*" + " *"*(n-k) + "*"*(3+4*(k-2)) +"* "*(n-k) +"*")
    
Star(n)

